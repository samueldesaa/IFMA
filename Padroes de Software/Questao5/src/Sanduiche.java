public abstract class Sanduiche {
    String pao;
    String queijo;
    String presunto;
    String salada;

    public void mostrar() {
        System.out.println("Pão: " + pao);
        System.out.println("Queijo: " + queijo);
        System.out.println("Presunto: " + presunto);
        System.out.println("Salada: " + salada);
        System.out.println();
    }
}
